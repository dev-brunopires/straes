ALTER TRIGGER [sis].[T_SANTO_UPDATE]
   ON  [sis].[SANTO]
   AFTER UPDATE
AS
BEGIN
	SET NOCOUNT ON;

	BEGIN TRY
		BEGIN TRAN
			INSERT INTO [log].[SANTO] ( ID_SANTO, ID_PESSOA, ID_ORDEM, ID_ORIXA, FUNDAMENTO, USUARIO_OPERACAO, DATA_OPERACAO, STATUS_OPERACAO )
			SELECT ID_SANTO, ID_PESSOA, ID_ORDEM, ID_ORIXA, FUNDAMENTO, USUARIO_OPERACAO, GETDATE(), 'UPDATE' FROM INSERTED
		COMMIT TRAN
	END TRY

	BEGIN CATCH
		SELECT
			ERROR_NUMBER() AS ERRORNUMBER,
			ERROR_SEVERITY() AS ERRORSEVERITY,
			ERROR_STATE() AS ERRORSTATE,
			ERROR_PROCEDURE() AS ERRORPROCEDURE,
			ERROR_LINE() AS ERRORLINE,
			ERROR_MESSAGE() AS ERRORMESSAGE;

		ROLLBACK TRAN
	END CATCH

	SET NOCOUNT OFF;
END
GO


